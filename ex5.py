# Initializing personal details
name = "John Smith"
age = 46
height = 184 # in cm
weight = 84 # in kgs
eyes = "Blue"
hair = "Blonde"

# Printing  personal details, using f strings
# Printing name
print(f"Let's talk about {name}")

# Printing height
print(f"He's {height} cm tall")

# Printing weight
print(f"He's {weight} kgs heavy")

# Printing eyes and hair color
print(f"He's got {eyes} eyes and {hair} hair.")

# Tricky line
total = age + height + weight
print(f"If I add {age}, {height} and {weight}, the total is {total}")
